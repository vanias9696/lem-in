#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*mem;
	size_t	i;

	i = 0;
	mem = (char *)malloc(size + 1);
	if (mem == 0)
		return (0);
	while (i < size + 1)
	{
		mem[i] = '\0';
		i++;
	}
	return (mem);
}

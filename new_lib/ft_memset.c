#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	size_t	i;
	char	*temp;

	temp = b;
	i = 0;
	while (i < n)
	{
		temp[i] = c;
		i++;
	}
	return (temp);
}

#include "libft.h"

char	*ft_by_n(int n, char c)
{
	char	*s;
	int		i;

	i = 0;
	if (!(s = (char *)malloc(n + 1)))
		return (0);
	while (i < n)
	{
		s[i] = c;
		i++;
	}
	s[i] = '\0';
	return (s);
}

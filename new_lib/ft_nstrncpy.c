#include "libft.h"

char	*ft_nstrncpy(char *str1, const char *str2)
{
	int i;
	int k;

	i = 0;
	k = ft_strlen(str2) - 1;
	while (k != -1)
	{
		str1[i] = str2[k];
		i++;
		k--;
	}
	str1[i] = '\0';
	return (str1);
}

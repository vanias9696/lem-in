char	*ft_strrchr(const char *str, int s)
{
	int l;

	l = 0;
	while (str[l] != '\0')
		l++;
	while (l >= 0)
	{
		if (str[l] == s)
			return ((char *)str + l);
		else
			l--;
	}
	return (0);
}

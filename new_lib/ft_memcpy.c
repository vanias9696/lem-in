#include "libft.h"

void	*ft_memcpy(void *dest, const void *s, size_t n)
{
	size_t	i;
	char	*t1;
	char	*t2;

	t1 = (char *)dest;
	t2 = (char *)s;
	i = 0;
	while (i < n)
	{
		t1[i] = t2[i];
		i++;
	}
	return (dest);
}

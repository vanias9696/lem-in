#include "libft.h"

char	*ft_strstr(char *str1, char *str2)
{
	int i;
	int j;

	i = 0;
	while (str1[i] != '\0')
	{
		j = 0;
		while (str1[i + j] != '\0' && str1[i + j] == str2[j])
		{
			j++;
			if (str2[j] == '\0')
				return (str1 + i);
		}
		i++;
	}
	if (str2[0] == '\0')
		return (str1);
	return (0);
}
